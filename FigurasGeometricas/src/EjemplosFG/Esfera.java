package EjemplosFG;

public class Esfera {

	public static void main(String[] args) {
		//Nombrar variables
   float r;	
   double area,volumen;
   
   //Datos de entrada
   r=4;
   
   //Procesamiento
   area=4*Math.PI*Math.pow(r,2);
   volumen=4*Math.PI*Math.pow(r,3)/3;
   
   //Datos de salida
   
   System.out.println("Datos de la esfera");
   System.out.println("1.Area de la base: " + String.format("%.2f",area));
   System.out.println("2.Volumen de la esfera: "+String.format("%.2f",volumen));
		

	}

}
